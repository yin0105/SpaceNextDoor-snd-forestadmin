router.post('/actions/payouts/paid', permissionMiddlewareCreator.smartAction(), (req, res) => {
  return new RecordsGetter(payouts).getIdsFromRequest(req)
    .then((payoutIds) => {
      return payouts
        .update({ status: 'PAID' }, { where: { id: payoutIds } })
        .then(() => {
          res.send({ success: 'Payout is now Paid!' });
        });
    });
});

